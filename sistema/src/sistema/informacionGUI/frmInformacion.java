    private void btnInforme5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInforme5ActionPerformed
        conexion objtConexion = new conexion();

        try {
            ResultSet resultado = objtConexion.consultarRegistros("SELECT ID_Compra, Constructora, Banco_Vinculado\n" +
                "FROM Compra c\n" +
                "INNER JOIN Proyecto p ON c.ID_Proyecto = p.ID_Proyecto\n" +
                "WHERE Ciudad='Salento' AND Proveedor='Homecenter';");

            String [] titulos = {"ID Proyecto", "Constructora", "Banco vinculado" };
            model = new DefaultTableModel(null, titulos);
            tbInformes.setModel(model);

            while (resultado.next()) {
                System.out.println(resultado.getString("ID_Compra"));
                System.out.println(resultado.getString("Constructora"));
                System.out.println(resultado.getString("Banco_Vinculado"));

                Object [] usuario = {resultado.getString("ID_Compra"),resultado.getString("Constructora"), resultado.getString("Banco_Vinculado")};
                model.addRow(usuario);

            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnInforme5ActionPerformed

